#Actividad 4 – Asignación y operadores de asignación


# Valor inicial

$numero = 50
Write-Host "Valor inicial: $numero"



# Asignación directa

$numero = 100
Write-Host "Tras asignación directa (= 100): $numero"



# Incremento y decremento

$numero++
Write-Host "Tras incremento (++): $numero"
$numero--
Write-Host "Tras decremento (--): $numero"



# Asignación compuesta

$numero += 20
Write-Host "Tras asignación compuesta (+= 20): $numero"
$numero *= 2
Write-Host "Tras asignación compuesta (*= 2): $numero"